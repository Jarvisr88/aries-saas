        public int CalcFontIndex(string fontName, int doubleFontSize, bool fontBold, bool fontItalic, CharacterFormattingScript script, bool fontStrikeout, bool fontUnderline, int textRotation)
        {
            int num = this.unitConverter.PointsToFontUnits(doubleFontSize);
            int baseFontIndex = this.CalcNormalFontIndex(fontName, num, fontBold, fontItalic, fontStrikeout, fontUnderline, textRotation);
            if (script == CharacterFormattingScript.Normal)
            {
                return baseFontIndex;
            }
            int num3 = (script != CharacterFormattingScript.Subscript) ? this.CalcSuperscriptFontSize(baseFontIndex) : this.CalcSubscriptFontSize(baseFontIndex);
            return this.CalcFontIndexCore(fontName, num3 * 2, num, fontBold, fontItalic, script, fontStrikeout, fontUnderline, textRotation, this[baseFontIndex]);
        }

        protected internal virtual int CalcFontIndexCore(string fontName, int doubleFontSize, int doubleBaselineFontSize, bool fontBold, bool fontItalic, CharacterFormattingScript script, bool fontStrikeout, bool fontUnderline, int textRotation, FontInfo baselineFontInfo)
        {
            int num2;
            long key = this.CalcHash(fontName, doubleFontSize, doubleBaselineFontSize, fontBold, fontItalic, script, fontStrikeout, fontUnderline, textRotation);
            if (this.indexHash.TryGetValue(key, out num2))
            {
                return num2;
            }
            FontCache cache = this;
            lock (cache)
            {
                return (this.indexHash.TryGetValue(key, out num2) ? num2 : this.CreateFontInfo(key, fontName, doubleFontSize, fontBold, fontItalic, fontStrikeout, fontUnderline, textRotation, baselineFontInfo));
            }
        }

        protected internal virtual long CalcHash(string fontName, int doubleFontSize, int doubleBaselineFontSize, bool bold, bool italic, CharacterFormattingScript script, bool strikeout, bool underline, int textRotation)
        {
            long hashCode = fontName.ToLower().GetHashCode();
            long num2 = doubleFontSize + doubleBaselineFontSize;
            int num3 = ((((((textRotation + 90) << 6) | (underline ? 0x20 : 0)) | (strikeout ? 0x10 : 0)) | (italic ? 8 : 0)) | (bold ? 4 : 0)) | ((int) script);
            return (((hashCode << 0x20) + (num2 << 14)) + num3);
        }

        public int CalcNormalFontIndex(string fontName, int doubleFontSize, bool fontBold, bool fontItalic, bool fontStrikeout, bool fontUnderline, int textRotation) => 
            this.CalcFontIndexCore(fontName, doubleFontSize, doubleFontSize, fontBold, fontItalic, CharacterFormattingScript.Normal, fontStrikeout, fontUnderline, textRotation, null);

