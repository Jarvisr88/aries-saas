        protected virtual void Dispose(bool disposing)
        {
            if (disposing)
            {
                this.DisposeContent();
                if (this.measurer != null)
                {
                    this.measurer.Dispose();
                    this.measurer = null;
                }
            }
            this.isDisposed = true;
        }

        protected internal virtual void DisposeContent()
        {
            if (this.fontInfoList != null)
            {
                int count = this.fontInfoList.Count;
                int num2 = 0;
                while (true)
                {
                    if (num2 >= count)
                    {
                        this.fontInfoList.Clear();
                        this.fontInfoList = null;
                        break;
                    }
                    this[num2].Dispose();
                    num2++;
                }
            }
            if (this.indexHash != null)
            {
                this.indexHash.Clear();
                this.indexHash = null;
            }
        }

        public virtual string FindSubstituteFont(string sourceFontName, char ch)
        {
            this.PopulateNameToCharacterSetMap();
            int num = 0x7fffffff;
            string key = sourceFontName;
            Dictionary<string, FontCharacterSet> nameToCharacterSetMap = this.NameToCharacterSetMap;
            lock (nameToCharacterSetMap)
            {
                FontCharacterSet set = this.NameToCharacterSetMap[sourceFontName];
                foreach (KeyValuePair<string, FontCharacterSet> pair in this.NameToCharacterSetMap)
                {
                    FontCharacterSet set2 = pair.Value;
                    if (set2.ContainsChar(ch))
                    {
                        int num2 = FontCharacterSet.CalculatePanoseDistance(set, set2);
                        if (num2 < num)
                        {
                            num = num2;
                            key = pair.Key;
                        }
                    }
                }
            }
            return key;
        }

