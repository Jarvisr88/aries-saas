        internal static short GetGlyphCode(short str, PdfSimpleFontEncoding encoding)
        {
            short num;
            return (!TryGetGlyphCode(encoding.GetGlyphName((byte) str), out num) ? str : num);
        }

        internal static string GetGlyphName(short glyphCode)
        {
            string str;
            return (GlyphNames.TryGetValue((ushort) glyphCode, out str) ? str : ".notdef");
        }

        internal static bool TryGetGlyphCode(string glyphName, out short glyphCode)
        {
            ushort num;
            if (GlyphCodes.TryGetValue(glyphName, out num))
            {
                glyphCode = (short) num;
                return true;
            }
            int length = glyphName.Length;
            if (((length == 5) && (glyphName.StartsWith("u") || glyphName.StartsWith("U"))) && ushort.TryParse(glyphName.Substring(1), NumberStyles.HexNumber, CultureInfo.InvariantCulture, out num))
            {
                glyphCode = (short) num;
                return true;
            }
            if ((length == 7) && (uniRegex.IsMatch(glyphName) && ushort.TryParse(glyphName.Substring(3), NumberStyles.HexNumber, CultureInfo.InvariantCulture, out num)))
            {
                glyphCode = (short) num;
                return true;
            }
            glyphCode = 0;
            return false;
        }

        private static IDictionary<ushort, string> GlyphNames
        {
            get
            {
                if (glyphNames == null)
                {
                    Type type = typeof(PdfUnicodeConverter);
                    lock (type)
                    {
                        if (glyphNames == null)
                        {
                            glyphNames = new Dictionary<ushort, string>();
                            foreach (KeyValuePair<string, ushort> pair in GlyphCodes)
                            {
                                string key = pair.Key;
                                if (!excludedGlyphNames.Contains(key))
                                {
                                    glyphNames.Add(pair.Value, key);
                                }
                            }
                        }
                    }
                }
                return glyphNames;
            }
        }
